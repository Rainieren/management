    public function store(Request $request, $slug)
    {
        dd($request->all());
        $this->validate($request, [
            'token' => 'required'
        ]);

        $plan = Plan::where('slug', $slug)->first();

        $request->user()->newSubscription('default', $plan->stripe_plan_id)->create($request->token);

        return back();
    }

